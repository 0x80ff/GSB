	<h2>Gestion des frais des visiteurs</h2>
	<p>Bienvenue dans votre application de gestion des frais de déplacements. </p>
	<p>
		Afin de valider les frais engagés par les visiteurs lors de leurs déplacements professionnels, vous devez compléter des fiches de frais mensuelles.
		<ul>
			<li>Une fiche de frais court du 1er au dernier jour du mois des different visiteurs;</li>
			<li>Les fiches de frais sont créées automatiquement par l'application au fil des utilisations des visiteurs;</li>
			<li>Vous validerez les fiches de frais à votre rythme;</li>
			<li>Lorsqu'une fiche est totalement remboursée, changez le statut de cette derniere par "Validée";</li>
		</ul>
	</p>

	<p>
		Au moyen du bandeau gauche, vous avez accès aux fonctionalités 
		du profil comptable : 
		<ul>
			<li>Gérer les fiches de frais (changer l'état des rembourssements)</li>
			<li>Se déconnecter</li>
		</ul>
	</p>
</div>